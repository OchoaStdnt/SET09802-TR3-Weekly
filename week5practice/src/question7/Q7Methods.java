package question7;

public class Q7Methods {
	public static int addTogether(int iVal1, int iVal2, int iVal3) {
		int total = iVal1 + iVal2 + iVal3;
		return total;
	}//end addTogether integers
	
	public static double addTogether(double dVal1, double dVal2, double dVal3) {
		double dTotal = dVal1 + dVal2 + dVal3;
		return dTotal;
	}//end addTogether doubles
	
	public static String addTogether(String sVal1, String sVal2, String sVal3) {
		String fullString = sVal1 + sVal2 + sVal3;
		return fullString;
	}//end addTogther Strings

}//end class
